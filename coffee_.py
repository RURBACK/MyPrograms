def calculate_visitors(coffee, milk):
    # Переводим входные данные: килограмм кофе в граммы и литры молока в миллилитры
    coffee_g = coffee * 1000  
    milk_ml = milk * 1000
    
    # Вычисляем максимальное число возможных порций эспрессо исходя из количества кофе,
    # учитывая, что одна порция эспрессо требует 7 г кофе
    max_espresso = int(coffee_g // 7)
    
    # Создаем переменную для хранения числа обслуженных посетителей
    visitors = 0
    
    # Цикл по количеству приготовленных порций эспрессо
    for i in range(max_espresso):
        # Проверяем условие для приготовления напитка с молоком:
        # если номер порции делится на 3 или одновременно делится на 5 и не делится на 3
        if (i+1) % 3 == 0 or ((i+1) % 5 == 0 and not (i+1) % 3 == 0):
            # Напиток — капучино или латте
            if (i+1) % 3 == 0:
                # Готовится капучино, молоко расходуется меньше (100 мл)
                milk_needed = 100
            else:
                # Готовится латте, больше расход молока (180 мл)
                milk_needed = 180
            
            # Проверка наличия достаточного количества молока
            if milk_ml >= milk_needed:
                # Уменьшаем объем оставшегося молока и увеличиваем счётчик посетителей
                milk_ml -= milk_needed
                visitors += 1
            else:
                # Молоко закончилось, прекращаем цикл обслуживания
                break
        else:
            # Обычный американо (не тратит молоко), добавляем посетителя
            visitors += 1
        
    # Возвращаем итоговое количество обслуженных посетителей
    return visitors

# Пользователь вводит количество доступного кофе и молока
coffee_input = float(input("Введите количество молотого кофе (кг): "))
milk_input = float(input("Введите количество молока (л): "))

# Расчет максимального числа посетителей
result = calculate_visitors(coffee_input, milk_input)

# Печатаем итоговый результат
print(f"Кофейня сможет обслужить {result} посетителей.")